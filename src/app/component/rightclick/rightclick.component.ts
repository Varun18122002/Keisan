export class RightclickComponent {

  @Input() isVisible = false;
  @Input() position = { x: 0, y: 0 };
  @Input() selectedCells: any = null;
  @Output() menuAction = new EventEmitter<string>();

  constructor(private elementRef: ElementRef) {}

  onMenuItemClick(action: string) {
    this.menuAction.emit(action);
  }

  @HostListener('window:contextmenu', ['$event'])
  onContextMenu(event: MouseEvent) {

    event.preventDefault();

    const x = event.clientX;
    const y = event.clientY;
    const winWidth = window.innerWidth;
    const winHeight = window.innerHeight;
    const cmWidth = this.elementRef.nativeElement.offsetWidth;
    const cmHeight = this.elementRef.nativeElement.offsetHeight;
    const shareMenu = this.elementRef.nativeElement.querySelector('.wrapper')

    if(x > (winWidth - cmWidth - (shareMenu?.offsetHeight || 0))){
      shareMenu?.style.setProperty('left' ,'-200px');
    } else {
      shareMenu?.style.setProperty('left', '');
      shareMenu?.style.setProperty('right', '-200px');
    }

    this.position.x = x > winWidth - cmWidth ? winWidth - cmWidth - 5 : x;
    this.position.y = y > winHeight - cmHeight ? winHeight - cmHeight - 5 : y;

    this.isVisible = true;
  }

  @HostListener('document:click')
  onClick() : void{
    this.isVisible = false;
  }

}


